// Create search index for efficient autocomplete
export function createSearchIndex(cities: City[]) {
  return cities.map((city) => ({
    ...city,
    searchText: `${city.name} ${city.region ? city.region + " " : ""}${city.country}`.toLowerCase(),
    displayName: `${city.name}${city.region ? `, ${city.region}` : ""}, ${city.country}`,
    // Create a unique identifier for each city based on name, region, and country
    uniqueId: `${city.name.toLowerCase()}_${city.region ? city.region.toLowerCase() : ""}_${city.country.toLowerCase()}`
  }))
}

export function searchCities(query: string, cities: City[], limit = 10) {
  if (!query.trim()) return []

  const searchIndex = createSearchIndex(cities)
  const normalizedQuery = query.toLowerCase().trim()

  // Exact matches first
  const exactMatches = searchIndex.filter((city) => city.name.toLowerCase() === normalizedQuery)

  // Starts with matches
  const startsWithMatches = searchIndex.filter(
    (city) => city.name.toLowerCase().startsWith(normalizedQuery) && !exactMatches.some(m => m.uniqueId === city.uniqueId),
  )

  // Contains matches
  const containsMatches = searchIndex.filter(
    (city) =>
      city.searchText.includes(normalizedQuery) && 
      !exactMatches.some(m => m.uniqueId === city.uniqueId) && 
      !startsWithMatches.some(m => m.uniqueId === city.uniqueId),
  )

  // Combine all matches
  const allMatches = [...exactMatches, ...startsWithMatches, ...containsMatches]
  
  // Deduplicate based on uniqueId
  const uniqueIds = new Set()
  const dedupedResults = allMatches.filter(city => {
    if (uniqueIds.has(city.uniqueId)) {
      return false
    }
    uniqueIds.add(city.uniqueId)
    return true
  })
  
  // Sort by population (largest first) and limit results
  return dedupedResults
    .sort((a, b) => (b.population || 0) - (a.population || 0))
    .slice(0, limit)
}
